package org.dragon.skill.enums;

/**
 * 技能分类枚举。
 * 用于区分不同场景下的技能。
 *
 * @since 1.0
 */
public enum SkillCategory {
    /** 通用技能 */
    GENERAL,
    /** 文件操作类技能 */
    FILE_OPERATION,
    /** 网络请求类技能 */
    NETWORK,
    /** 数据处理类技能 */
    DATA_PROCESSING,
    /** 代码执行类技能 */
    CODE_EXECUTION,
    /** 系统管理类技能 */
    SYSTEM_ADMIN
}
