package org.dragon.memory.core;

/**
 * 记忆类型枚举
 *
 * @author binarytom
 * @version 1.0
 */
public enum MemoryType {
    /**
     * 用户反馈
     */
    FEEDBACK,

    /**
     * 项目信息
     */
    PROJECT,

    /**
     * 参考资料
     */
    REFERENCE,

    /**
     * 会话摘要
     */
    SESSION_SUMMARY,

    /**
     * 工作空间决策
     */
    WORKSPACE_DECISION,

    /**
     * 角色配置文件
     */
    CHARACTER_PROFILE
}
